// 模拟分页响应
export const mockPageResult = <T>(list: T[], page: number, size: number): PageResult<T> => {
  const start = (page - 1) * size
  const end = start + size
  return {
    list: list.slice(start, end),
    total: list.length,
    page,
    size,
  }
}

